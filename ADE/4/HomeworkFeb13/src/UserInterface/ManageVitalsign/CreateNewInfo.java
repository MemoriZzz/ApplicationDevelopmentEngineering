    private void createBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtnActionPerformed
        // TODO add your handling code here:
        
        if(  rrTxt.getText().equals("") || hrTxt.getText().equals("") ||sbpTxt.getText().equals("") || wipTxt.getText().equals("") )
            { JOptionPane.showMessageDialog(null,"one or more fields are empty"); }
        
        else{
        
            int rr;
            try{
                rr = Integer.parseInt(rrTxt.getText());
            }catch(NumberFormatException e){
                JOptionPane.showMessageDialog(null,"Please input a valid number for Respiratory Rate");
                return;
            }
        
            int hr;
            try{
                hr = Integer.parseInt(hrTxt.getText());
            }catch(NumberFormatException e){
                JOptionPane.showMessageDialog(null,"Please input a valid number for Heart Rate");
                return;
            }
        
            int sbp;
            try{
                sbp = Integer.parseInt(sbpTxt.getText());
            }catch(NumberFormatException e){
                JOptionPane.showMessageDialog(null,"Please input a valid number for Systolic Blood Pressure");
                return;
            }
        
            double wip;
            try{
                wip = Integer.parseInt(wipTxt.getText());
            }catch(NumberFormatException e){
                JOptionPane.showMessageDialog(null,"Please input a valid number for Weight");
                return;
            }
        

        
        
        
            Vitalsign vitalsign = patient.getVitalsignCatalog().addVitalsign();
            
            vitalsign.setDate(getTime());
            vitalsign.setRr(Integer.parseInt(rrTxt.getText()));
            vitalsign.setHr(Integer.parseInt(hrTxt.getText()));
            vitalsign.setSbp(Integer.parseInt(sbpTxt.getText()));
            vitalsign.setWip(Double.parseDouble(wipTxt.getText()));
            vitalsign.setWik((Double.parseDouble(wipTxt.getText()))*(0.45));
        
            wikTxt.setText(Double.toString((Double.parseDouble(wipTxt.getText()))*(0.45)));
        

            JOptionPane.showMessageDialog(null, "Added");
        
            }
        
    }//GEN-LAST:event_createBtnActionPerformed

    
    private void backAction(){
        
        rightPanel.remove(this);
        Component[] componentArray = rightPanel.getComponents();
        Component component = componentArray[componentArray.length-1];
        ManageVitalsignWorkArea manageVitalsignWorkArea = (ManageVitalsignWorkArea) component;
        manageVitalsignWorkArea.refreshTable();
        CardLayout layout = (CardLayout) rightPanel.getLayout();
        layout.previous(rightPanel);
    }
    
    private String getTime(){
        String time;
        Date d = new Date();
        SimpleDateFormat dateFormat= new SimpleDateFormat("yyyy-MM-dd :hh:mm:ss");
        time = dateFormat.format(d);
        return time;
    }

