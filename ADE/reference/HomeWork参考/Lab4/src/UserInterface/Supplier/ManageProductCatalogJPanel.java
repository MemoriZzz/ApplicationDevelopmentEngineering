    private void btnViewProductDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewProductDetailsActionPerformed
        int selectedRow = ProductTable.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"Please select a row from table to view details",
                    "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        else{
            Product p = (Product) ProductTable.getValueAt(selectedRow, 0);
            ViewProductJPanel vpjp = new ViewProductJPanel(userProcessContainer,p);
            userProcessContainer.add("ViewProductJPanel",vpjp);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnViewProductDetailsActionPerformed

    private void btnDeleteProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteProductActionPerformed
        int selectedRow = ProductTable.getSelectedRow();
        if(selectedRow>=0){
            int dialogButton = JOptionPane.YES_NO_CANCEL_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null,"Would you like to delete the account",
                      "Warning", dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
                Product p = (Product) ProductTable.getValueAt(selectedRow, 0);
                supplier.getProductCatalog().removeProduct(p);
                refreshTable();
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"Please select a row from table first",
                    "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
    }//GEN-LAST:event_btnDeleteProductActionPerformed

