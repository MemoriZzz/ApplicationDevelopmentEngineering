        if(selectedrow>=0)
        {
            Resume re = (Resume)resumeTable.getValueAt(selectedrow,0);
            rc.deleteResume(re);
            JOptionPane.showMessageDialog(null,"Vital sign has been deleted");
            populateTable();
            firstNameTextField.setText("");
            lastNameTextField.setText("");
            sexTextField.setText("");
            ageTextField.setText("");
            affiliationTextField.setText("");
            majorTextField.setText("");
            dobTextField.setText("");
            workYearTextField.setText("");
            careerObjectiveStatementTextArea.setText("");
            streetln1TextField.setText("");
            streetln2TextField.setText("");
            cityTextField.setText("");
            countryTextField.setText("");
            degree1TextField.setText("");
            degreeDate1TextField.setText("");
            degree2TextField.setText("");
            degreeDate2TextField.setText("");
            programTextField.setText("");
            
            pictureLabel1.setIcon(null);
        }
        else
            JOptionPane.showMessageDialog(null,"Please select any row");
