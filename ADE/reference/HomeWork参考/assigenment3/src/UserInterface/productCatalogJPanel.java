private void populateTable(){
         DefaultTableModel dtm = (DefaultTableModel) tblCatalog.getModel();
         dtm.setRowCount(0);
         for(Product account: productDirectory.getAccountlist()){
             //System.out.println(account.getProductCatalog());
            if(productCatalog.equals(account.getProductCatalog()))
               productList.add(account);
        }
         for(Product account: productList){
             Object[] row = new Object[3];
             row[0] = account;
             row[1] = account.getModelNumber();
             row[2] = account.getVendorName();
             dtm.addRow(row);
         }
     }

