    private void tblViewDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tblViewDetailsActionPerformed
        int selectedrow = tblCatalog.getSelectedRow();

        if(selectedrow>=0)
        {
        Product account = (Product)tblCatalog.getValueAt(selectedrow,0);
        txtProductName.setText(account.getProductName());
        txtModelNumber.setText(account.getModelNumber());
        txtVendorName.setText(account.getVendorName());
        txtDescription.setText(account.getDescription());
         txtBasePrice.setText(String.valueOf(account.getBasePrice()));
        txtCeilingPrice.setText(String.valueOf(account.getCeilingPrice()));
        txtFloorPrice.setText(String.valueOf(account.getFloorPrice()));
        txtProductCatalog.setText(account.getProductCatalog());
        featureJTextArea.setText(account.getFeature());

      
        }
        else
        JOptionPane.showMessageDialog(null,"Please select any row");// TODO add your handling code here:
    }//GEN-LAST:event_tblViewDetailsActionPerformed

