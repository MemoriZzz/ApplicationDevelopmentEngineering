  private void populateTable(){
         DefaultTableModel dtm = (DefaultTableModel) tblVendor.getModel();
         dtm.setRowCount(0);
         for(Product product: vendorList){
             Object[] row = new Object[3];
             row[0] = product;
             row[1] = product.getModelNumber();
             row[2] = product.getVendorName();
             vendorName = product.getVendorName();
             dtm.addRow(row);
         }
     }
