    private void chooseBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chooseBtnActionPerformed
        
        //choose a photo
        JFileChooser jFileChooser = new JFileChooser();
        
        int parameter = jFileChooser.showOpenDialog(null);
        if (parameter == JFileChooser.APPROVE_OPTION);{
            File f = jFileChooser.getSelectedFile();
    
        try{
            Image img;
            img = ImageIO.read(f);
            Image image = img.getScaledInstance(100, 100, Image.SCALE_DEFAULT);
            photoLabel.setIcon(new ImageIcon(image));
            photoLabel.setSize(100,100);
            attributes.setPhoto(f);
            
        }
        catch (IOException | NullPointerException npex){}
    
    }
        
        
        
    }//GEN-LAST:event_chooseBtnActionPerformed

